package task;

/**
 * класс с основной логикой вычислений
 */
public class OtherClass {
    /**
     * Вычитание
     *
     * @param a - уменьшаемое
     * @param b - Вычитаемое
     * @return - разность
     */
    public static int sub(int a, int b) {
        return a - b;
    }

    /**
     * @param a - слагаемое 1
     * @param b - слагаемое 2
     * @return - сумма
     */
    public static int add(int a, int b) {
        return a + b;
    }

    /**
     * @param a - делимое
     * @param b - делитель
     * @return - частное
     */
    public static int div(int a, int b) {
        return a / b;
    }

    /**
     * @param a - множитель 1
     * @param b - множитель 2
     * @return - произведение
     */
    public static int mul(int a, int b) {
        return a * b;
    }

}
